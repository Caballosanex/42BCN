#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}

/* If the list is empty, then the last element is NULL.
The while loop will run until the last element of the list is reached.
The last element of the list is returned. */