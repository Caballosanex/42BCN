#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (lst)
	{
		del(lst->content);
		free(lst);
	}
}

/* We check if the list is not empty. If it is, then we return.
We delete the content of the list using the function del.
We free the list using the function free.
We return. */